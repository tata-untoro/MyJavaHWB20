package org.example.e23;

public class E23StringEqualsIfConditions {
    public static void main(String[] args) {
        // Declare a string variable and assign a value
        String language;

        // Check if the string equals "Java"
        language = "Java";

        if (language.equals("Java")) {
            System.out.println("The programming language is " + language + ".");
        } else {
            System.out.println("The programming language is not " + language + ".");
        }

    }
}
